$(function () {
    $('#pic .upload_input').on("change", function () {
        if(this.files.length > 0) {
            $('#pic .upload_img').attr("src", window.URL.createObjectURL(this.files[0]));
            $('#pic .upload_content').addClass('hide');
            $('#pic .upload_img').removeClass('hide');
        } 
    })

})




