
$(function () {
    $('#uppc1 .upload_input').on("change", function () {
        if(this.files.length > 0) {
            $('#uppc1 .upload_img').attr("src", window.URL.createObjectURL(this.files[0]));
            $('#uppc1 .upload_content').addClass('hide');
            $('#uppc1 .upload_img').removeClass('hide');
        } 
    })

})
